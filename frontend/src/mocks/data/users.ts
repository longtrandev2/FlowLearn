// Helper functions for filtering
export const getUsersByStatus = (status: UserStatus) =>
  mockUsers.filter(user => user.status === status);

export const getUsersByPlan = (plan: UserPlan) =>
  mockUsers.filter(user => user.plan === plan);

export const getUsersByRole = (role: UserRole) =>
  mockUsers.filter(user => user.role === role);

export const getUserById = (id: string) =>
  mockUsers.find(user => user.id === id);

export const searchUsers = (query: string) =>
  mockUsers.filter(user =>
    user.email.toLowerCase().includes(query.toLowerCase()) ||
    user.fullName.toLowerCase().includes(query.toLowerCase())
  );
